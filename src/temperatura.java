public class temperatura {
    public static void main(String[] args) {

        //Conversor de temperatura C° para Fahrenheit.

        double temp;
        double celsius = 10;


        temp = (celsius * 1.8) + 32;

        System.out.println("O valor em Celsius é "+ celsius + "F°, em Fahrenheit fica " + temp + "C°");


    }



}
